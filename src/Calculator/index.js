const calculadora = {
    somar(numberLeft, numberRight) {
      return numberLeft + numberRight;
    },
    subtrair(numberLeft, numberRight) {
      return numberLeft - numberRight;
    },
    dividir(numberLeft, numberRight) {
      return numberLeft / numberRight;
    },
    multiplicar(numberLeft, numberRight) {
      return numberLeft * numberRight;
    },
  };
  
  module.exports = calculadora;
  